HertzTown = {}

function HertzTown.Cooldown()
    COMMON.RespawnAllies()
    GAME:CutsceneMode(true)
    local azura = CH("Teammate1")
    local rexio = CH("Teammate2")
    local maru = CH("PLAYER")
    local gonk = CH("Gonk")
    local function haprex()
       EXPLCOMMON.CharHop("Teammate2")
    end
    local coro3 = TASK:BranchCoroutine(function() 
        GROUND:AnimateToPosition(azura, "Walk", Dir8.Up, azura.Position.X, azura.Position.Y - 45, 0.3, 0.6, 0)
        end)
    local coro4 = TASK:BranchCoroutine(function()
        GROUND:AnimateToPosition(rexio, "Walk", Dir8.Up, rexio.Position.X, rexio.Position.Y - 45, 0.3, 0.6, 0)
        end)
    local coro5 = TASK:BranchCoroutine(function()
        GAME:WaitFrames(25)
        GROUND:AnimateToPosition(maru, "Walk", Dir8.Up, maru.Position.X, maru.Position.Y - 65, 0.3, 0.6, 0)
        GROUND:CharTurnToCharAnimated(azura, maru, 4)
        GROUND:CharTurnToCharAnimated(rexio, maru, 4)
        end)
    local coro6 = TASK:BranchCoroutine(function()
        local the1 = TASK:BranchCoroutine(function()
            GAME:WaitFrames(50)
           EXPLCOMMON.SetCharAndEmotion(rexio, "Happy")
            UI:WaitShowDialogue("See, this is much better.[pause=50][script=0] No sand, no sun!", {haprex})

           EXPLCOMMON.SetCharAndEmotion(maru, "Normal")
            UI:WaitShowDialogue("Roofs are cool.")

            GROUND:CharTurnToCharAnimated(maru, rexio, 4)
           EXPLCOMMON.SetCharAndEmotion(rexio, "Worried")
            UI:WaitShowDialogue("Some psuedoguild leader could take some advice.[pause=50] Whatever,[emote=Happy] we should get to treasure hunting!")

            GROUND:CharTurnToCharAnimated(maru, azura, 4)
           EXPLCOMMON.CharHappyHop("Teammate1")
            local function turn()
               EXPLCOMMON.FaceEachother(azura, rexio)
            end
           EXPLCOMMON.SetCharAndEmotion(azura, "Joyous")
            UI:WaitShowDialogue("Yippee![pause=70] [emote=Worried]Wait...[script=0] the tent has a roof.", {turn})

            GROUND:CharTurnToCharAnimated(maru, gonk, 4)
           EXPLCOMMON.SetCharAndEmotion(rexio, "Worried")
            UI:WaitShowDialogue("Doesn't count...!")

            GAME:WaitFrames(30)
            EXPLCOMMON.CharSweatdrop("PLAYER")
           EXPLCOMMON.SetCharAndEmotion(maru, "Stunned")
            UI:WaitShowDialogue("...uh oh.")

            local function turn2()
                GROUND:CharTurnToCharAnimated(rexio, gonk, 4)
            end
           EXPLCOMMON.SetCharAndEmotion(rexio, "Worried")
            UI:WaitShowDialogue("Huh,[pause=45][script=0][emote=Happy] oh, pfft.[pause=35] I've got this!", {turn2})
            end)
        local the2 = TASK:BranchCoroutine(function()
            GAME:WaitFrames(30)
            GAME:FadeIn(90)
            end)
        TASK:JoinCoroutines({the1, the2})
        end)
    TASK:JoinCoroutines({coro3, coro4, coro5, coro6})

    local coro00 = TASK:BranchCoroutine(function()
        GROUND:MoveToPosition(maru, gonk.Position.X, gonk.Position.Y + 35, false, 1)
        end)
    local coro10 = TASK:BranchCoroutine(function()
        GAME:WaitFrames(25)
        GROUND:MoveToPosition(rexio, gonk.Position.X + 30, gonk.Position.Y + 25, false, 1)
        GROUND:CharTurnToCharAnimated(rexio, gonk, 4)
        end)
    local coro20 = TASK:BranchCoroutine(function()
        GAME:WaitFrames(30)
        GROUND:MoveToPosition(azura, gonk.Position.X - 30, gonk.Position.Y + 25, false, 1)
        GROUND:CharTurnToCharAnimated(azura, gonk, 4)
        end)
    local coro30 = TASK:BranchCoroutine(function()
        GAME:WaitFrames(25)
       EXPLCOMMON.SetCharAndEmotion(rexio, "Happy")
        UI:WaitShowDialogue("Hey,[pause=45] hey Mr. Big Guy.[pause=35] We're lookin' for treasure, so you should let us in!")
        end)
    TASK:JoinCoroutines({coro00, coro10, coro20, coro30})

   EXPLCOMMON.SetCharAndEmotion(gonk, "Normal")
    UI:WaitShowDialogue("[speed=0.5]Please present your entry passes.")

   EXPLCOMMON.CharQuestion("Teammate2")
   EXPLCOMMON.SetCharAndEmotion(rexio, "Stunned")
    UI:WaitShowDialogue("[speed=0.6]Huh...?[pause=45] Our what...?")

   EXPLCOMMON.SetCharAndEmotion(gonk, "Normal")
    UI:WaitShowDialogue("[speed=0.5]Please present your entry passes.")

   EXPLCOMMON.SetCharAndEmotion(maru, "Stunned")
    UI:WaitShowDialogue("Umm?[pause=45] What if we don't have any?")

   EXPLCOMMON.SetCharAndEmotion(gonk, "Normal")
    UI:WaitShowDialogue("[speed=0.5]Then you may purchase one to the left.")

   EXPLCOMMON.CharHop("Teammate2")
   EXPLCOMMON.SetCharAndEmotion(rexio, "Angry")
    UI:WaitShowDialogue("We nyeeeeed the treasure, though! Let us through!")

   EXPLCOMMON.SetCharAndEmotion(gonk, "Sigh")
    UI:WaitShowDialogue("[speed=0.5]I don't get paid enough to either care or know about what you are talking about")

    local function fake()
        GROUND:CharTurnToCharAnimated(rexio, maru, 4)
        EXPLCOMMON.CharSweatdrop("Teammate2")
    end
   EXPLCOMMON.SetCharAndEmotion(maru, "Normal")
    UI:WaitShowDialogue("We got sent on a mission.[pause=45] Someone stole treasure here and it belongs to our guild[script=0]leader's friend.", {fake})

    GROUND:CharTurnToCharAnimated(rexio, gonk, 4)
    UI:WaitShowDialogue("Did a thiefy guy run by here?")

   EXPLCOMMON.SetCharAndEmotion(gonk, "Normal")
    UI:WaitShowDialogue("[speed=0.5]No.[pause=40] Now please purchase a pass.")

   EXPLCOMMON.FaceEachother(rexio, maru)
   EXPLCOMMON.SetCharAndEmotion(rexio, "Stunned")
    UI:WaitShowDialogue("Wow.")
   EXPLCOMMON.AllyFollow(false, false)
    GAME:CutsceneMode(false)
end